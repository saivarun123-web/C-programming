//write a c program to multiplication of two matrices
#include<stdio.h>
int main()
{
	int a[12][12],b[12][12],c[12][12],i,j,k,r1,r2,c1,c2;
	scanf("%d%d",&r1,&c1);
	scanf("%d%d",&r2,&c2);
	if(c1!=r2)
	{
		printf("Matrix multiplication not possible");
		return 0;
	}
	for(i=0;i<r1;i++){
	for(j=0;j<c1;j++){
	 scanf("%d",&a[i][j]);
      }
}
for(i=0;i<r2;i++){
	for(j=0;j<c2;j++){
	 scanf("%d",&b[i][j]);
      }
}
for(i=0;i<r1;i++)
   {
for(j=0;j<c2;j++){
	c[i][j]=0;
           }
    }
for(i=0;i<r1;i++){
	for(j=0;j<c2;j++){
		for(k=0;k<c1;k++){
			c[i][j] +=a[i][k] *b[k][j];
		}
	}
}
for(i=0;i<r1;i++){
	for(j=0;j<c2;j++)
	{
		printf("%d\t",c[i][j]);
	}
	printf("\n");
}
return 0;
}
